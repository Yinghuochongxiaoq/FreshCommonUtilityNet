    /// <summary>
    /// 解析身份证
    /// </summary>
    public class IdCardNumber
    {
        private string _province;

        /// <summary>
        /// 所在省份信息
        /// </summary>
        public string Province
        {
            get { return _province; }
            set { _province = value; }
        }

        private string _area;

        /// <summary>
        /// 所在地区信息
        /// </summary>
        public string Area
        {
            get { return _area; }
            set { _area = value; }
        }

        private string _city;

        /// <summary>
        /// 所在区县信息
        /// </summary>
        public string City
        {
            get { return _city; }
            set { _city = value; }
        }

        private DateTime _age;

        /// <summary>
        /// 年龄
        /// </summary>
        public DateTime Age
        {
            get { return _age; }
            set { _age = value; }
        }

        private int _sex;

        /// <summary>
        /// 性别，0为女，1为男
        /// </summary>
        public int Sex
        {
            get { return _sex; }
            set { _sex = value; }
        }

        private string _cardnumber;

        /// <summary>
        /// 身份证号码
        /// </summary>
        public string CardNumber
        {
            get { return _cardnumber; }
            set { _cardnumber = value; }
        }

        private string _json;

        /// <summary>
        /// 生成Javascript对象；
        /// </summary>
        public string Json
        {
            get { return _json; }
            set { _json = value; }
        }

        /// <summary>
        /// 解析身份证
        /// </summary>
        private void _analysis()
        {
            //取省份，地区，区县
            string provCode = _cardnumber.Substring(0, 2).PadRight(6, '0');
            string areaCode = _cardnumber.Substring(0, 4).PadRight(6, '0');
            string cityCode = _cardnumber.Substring(0, 6).PadRight(6, '0');
            foreach (string[] t in IdCardValidatorHelper.FillAreas())
            {
                if (provCode == t[0])
                    _province = t[1];
                if (areaCode == t[0])
                    _area = t[1];
                if (cityCode == t[0])
                    _city = t[1];
                if (_province != null && _area != null && _city != null) break;
            }
            //取年龄
            string ageCode = _cardnumber.Substring(6, 8);
            try
            {
                int year = Convert.ToInt16(ageCode.Substring(0, 4));
                int month = Convert.ToInt16(ageCode.Substring(4, 2));
                int day = Convert.ToInt16(ageCode.Substring(6, 2));
                _age = new DateTime(year, month, day);
            }
            catch
            {
                _age = new DateTime(1900, 1, 1);
            }
            //取性别
            string orderCode = _cardnumber.Substring(14, 3);
            _sex = Convert.ToInt16(orderCode) % 2 == 0 ? 0 : 1;
            //生成Javascript对象
            _json = @"prov:'{0}',area:'{1}',city:'{2}',year:{3},month:{4},day:{5},sex:{6},number:'{7}'";
            _json = string.Format(_json, _province, _area, _city, _age.Year, _age.Month, _age.Day, _sex, _cardnumber);
            _json = "{" + _json + "}";
        }

        /// <summary>
        /// 构造函数解析身份证号
        /// </summary>
        /// <param name="idCardNumber"></param>
        public IdCardNumber(string idCardNumber)
        {
            _cardnumber = idCardNumber;
            _analysis();
        }
    }

